function [medSummary] = analyzeMeds(participantList, dataDir)

% Load subj summary excel sheet
% Analyzing meds in this file as this requires a more detailed analysis
% than other subjSummary items
subjSummaryFile = fullfile(dataDir,'/FLIC_SubjectSummary.xlsx'); 

% Detect the default options for this file
opts = detectImportOptions(subjSummaryFile);

% Set where the Variable Names (titles) and Data start
% Headers are in Row 1 and data starts in Row 2:
opts.VariableNamesRange = 'A1'; 
opts.DataRange = 'A2';

% Read the Excel file into a table
subjSummaryT = readtable(subjSummaryFile, opts);

% Rename variables to clean names
subjSummaryT = renamevars(subjSummaryT, {'SubjectID__'},{'SubjectID'});

% Last subject is FLIC_0051, want the table to end here
idx_end = find(strcmp(subjSummaryT.SubjectID, 'FLIC_0051'));
subjSummaryT = subjSummaryT(1:idx_end, :);

% Keep only subjects in participantList
subjSummaryT = subjSummaryT(ismember(subjSummaryT.SubjectID, participantList), :);

% Convert text columns to string arrays
subjSummaryT.MigraineOrControl_ = string(subjSummaryT.MigraineOrControl_);
% Extract prescription medications column
subjSummaryT = renamevars(subjSummaryT, ...
    'PrescriptionMedications__including_asNeeded_MedicationsTakenWit', ...
    'medList');
subjSummaryT.medList = string(subjSummaryT.medList);

% Sort order to be migrainers -> control subjects in increasing order
subjSummaryT.idNum = str2double(extractAfter(subjSummaryT.SubjectID,"FLIC_"));
subjSummaryT = sortrows(subjSummaryT, {'MigraineOrControl_','idNum'});
subjSummaryT.idNum = []; % remove temp variable

cats = [ ...
    "Tricyclics", ...
    "SNRI/SSRI", ...
    "Beta blockers", ...
    "Anti-seizure", ...
    "CGRP mAb", ...
    "Botox", ...
    "Nurtec", ...
    "Triptans", ...
    "CGRP gepants", ...
    "Antiemetics", ...
    "NSAID/APAP/Excedrin"];

medClass = [ ...
    "Preventive", ...
    "Preventive", ...
    "Preventive", ...
    "Preventive", ...
    "Preventive", ...
    "Preventive", ...
    "Both", ...
    "Abortive", ...
    "Abortive", ...
    "Abortive", ...
    "Abortive"];

countsMigraine = zeros(length(cats),1);
countsControl  = zeros(length(cats),1);

% Tracking whether subjects are on any migraine meds
nMigraineAnyMed = 0;
nControlAnyMed = 0;

for i = 1:height(subjSummaryT)

    med = lower(string(subjSummaryT.medList(i)));

    % Preventive
    hasTricyclic = contains(med,[ ...
        "amitriptyline", ...
        "elavil", ...
        "nortriptyline", ...
        "pamelor"]);

    hasSNRI_SSRI = contains(med,[ ...
        "venlafaxine", ...
        "effexor", ...
        "duloxetine", ...
        "cymbalta", ...
        "sertraline", ...
        "zoloft", ...
        "fluoxetine", ...
        "prozac", ...
        "escitalopram", ...
        "lexapro", ...
        "citalopram", ...
        "celexa", ...
        "paroxetine", ...
        "paxil"]);

    hasBetaBlocker = contains(med,[ ...
        "propranolol", ...
        "inderal", ...
        "metoprolol", ...
        "lopressor", ...
        "toprol"]);

    hasAntiSeizure = contains(med,[ ...
        "topiramate", ...
        "topamax", ...
        "divalproex", ...
        "depakote", ...
        "valproate"]);

    hasCGRP_mAb = contains(med,[ ...
        "erenumab", ...
        "aimovig", ...
        "fremanezumab", ...
        "ajovy", ...
        "galcanezumab", ...
        "emgality", ...
        "eptinezumab", ...
        "vyepti"]);

    hasBotox = contains(med,[ ...
        "botox", ...
        "onabotulinumtoxin"]);

    % Nurtec is both preventive and abortive
    hasNurtec = contains(med,[ ...
        "rimegepant", ...
        "nurtec"]);

    % Abortive
    hasTriptan = contains(med,[ ...
        "sumatriptan", ...
        "imitrex", ...
        "rizatriptan", ...
        "maxalt", ...
        "zolmitriptan", ...
        "zomig", ...
        "eletriptan", ...
        "relpax", ...
        "almotriptan", ...
        "axert", ...
        "naratriptan", ...
        "amerge", ...
        "frovatriptan", ...
        "frova"]);

    hasCGRP_Gepant = contains(med,[ ...
        "ubrogepant", ...
        "ubrelvy"]);

    hasAntiemetic = contains(med,[ ...
        "ondansetron", ...
        "zofran", ...
        "metoclopramide", ...
        "reglan", ...
        "prochlorperazine", ...
        "compazine", ...
        "promethazine", ...
        "phenergan"]);

    hasNSAID_Acetaminophen_Excedrin = contains(med,[ ...
        "ibuprofen", ...
        "advil", ...
        "motrin", ...
        "naproxen", ...
        "aleve", ...
        "aspirin", ...
        "celecoxib", ...
        "celebrex", ...
        "acetaminophen", ...
        "tylenol", ...
        "excedrin"]);

    rowCounts = [ ...
        hasTricyclic ...
        hasSNRI_SSRI ...
        hasBetaBlocker ...
        hasAntiSeizure ...
        hasCGRP_mAb ...
        hasBotox ...
        hasNurtec ...
        hasTriptan ...
        hasCGRP_Gepant ...
        hasAntiemetic ...
        hasNSAID_Acetaminophen_Excedrin];

    hasAnyTrackedMedication = any(rowCounts);

    if strcmpi(subjSummaryT.MigraineOrControl_(i),"migraine")

        countsMigraine = countsMigraine + rowCounts';

        if hasAnyTrackedMedication
            nMigraineAnyMed = nMigraineAnyMed + 1;
        end

    elseif strcmpi(subjSummaryT.MigraineOrControl_(i),"control")

        countsControl = countsControl + rowCounts';

        if hasAnyTrackedMedication
            nControlAnyMed = nControlAnyMed + 1;
        end

    end

end

fprintf('Migraine participants on any tracked medication: %d\n', ...
    nMigraineAnyMed);

fprintf('Control participants on any tracked medication: %d\n', ...
    nControlAnyMed);

medSummary = table(medClass', ...
    cats', ...
    countsMigraine, ...
    countsControl, ...
    'VariableNames', ...
    {'Class','Category','Migraine','Control'});

end
